package racingcar.Model;

public class Input {
    private String[] names;
    private int attempt;

    public void setNames(String[] names) {
        this.names = names;
    }

    public void setAttempt(int attempt) {
        this.attempt = attempt;
    }

    public String[] getNames() {
        return this.names;
    }

    public int getAttempt() {
        return this.attempt;
    }
}
